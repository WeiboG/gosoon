package com.gosoon.util;

public class MyRequestCallback {

	MyResult mResult = new MyResult();
	public void onStart(MyResult result){mResult = result;};
	public void onCancelled(MyResult result){mResult = result;};
	public void onLoading(MyResult result){mResult = result;};
	public void onSuccess(MyResult result){mResult = result;};
	public void onFailure(MyResult result){mResult = result;};
	public void onEnd(MyResult result){mResult = result;};
	public MyResult getMyResult() {
		return mResult;
	}
}
